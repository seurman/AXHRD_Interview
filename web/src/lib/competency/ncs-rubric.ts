/** 역량 기본 루브릭 — L3 기준 (중간 난이도) */
const FALLBACK_RUBRIC = [
  "역량과 관련된 구체적 상황·과제를 제시했는가",
  "본인이 직접 수행한 행동을 명확히 서술했는가",
  "수치나 결과로 성과를 뒷받침했는가",
];

export function rubricForNcsLevel(competency: string, level?: number): string[] {
  const extended = NCS_EXTENDED_RUBRICS[competency];
  const map = extended ?? NCS_RUBRICS_BY_LEVEL[competency as CompetencyCode];
  if (!map) return [...FALLBACK_RUBRIC];
  if (level != null) {
    const lv = map[String(level)];
    if (lv?.length) return [...lv];
  }
  const mid = map["3"] ?? map.default;
  if (mid?.length) return [...mid];
  return [...FALLBACK_RUBRIC];
}

export function buildNcsRubric(competency: string): string[] {
  return rubricForNcsLevel(competency, 3);
}

export const NCS_SOURCE =
  "NCS 직업기초능력 정의 — 한국산업인력공단(2015, ncs.go.kr, KOGL 제1유형)";

/** 관리자 JSON 일괄 업로드용 */
export function ncsRubricImportFile() {
  const core = (Object.keys(NCS_RUBRICS_BY_LEVEL) as CompetencyCode[]).map((code) => ({
    code,
    levels: NCS_RUBRICS_BY_LEVEL[code],
  }));
  const extended = Object.entries(NCS_EXTENDED_RUBRICS).map(([code, levels]) => ({
    code,
    levels,
  }));
  return {
    version: 1,
    description: NCS_SOURCE,
    competencies: [...core, ...extended],
  };
}
